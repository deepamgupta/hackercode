    private void addStudentButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentButton1ActionPerformed
        String getFirstName = fname.getText();
        String getLastName = lname.getText();
        String getFatherName = fatherName.getText();
        String getContact = contact.getText();
        String getAddress = address.getText();
        String getCollege = college.getText();
        String getFeeStatus = feestatus.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Student", "root", "");
            String sql = ("Insert into student (First_Name, Last_name, Father_Name, Contact, Address, College, Fee_status) values(?,?,?,?,?,?,?);");

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, getFirstName);
            ps.setString(2, getLastName);
            ps.setString(3, getFatherName);
            ps.setString(4, getContact);
            ps.setString(5, getAddress);
            ps.setString(6, getCollege);
            ps.setString(7, getFeeStatus);
            Boolean flag = ps.execute();
            if (!flag) {
                System.out.println("Done");
            }

        } catch (Exception ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }    }//GEN-LAST:event_addStudentButton1ActionPerformed

